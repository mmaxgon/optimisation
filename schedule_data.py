"""
schedule_data.py — Данные для задачи оптимизации расписания кинотеатра.

Определяет все входные данные задачи:
- Временную сетку (T = 288 шагов по 5 минут = 24 часа)
- Фильмы (20 штук, два формата A и B, случайная длительность 1–3 часа)
- Залы (10 штук, с поддержкой разных форматов)
- Матрицу ожидаемых продаж sales[movie][time]
- Индексы совместимости (какие фильмы можно показать в каких залах)
- Верхние границы числа сеансов на каждом уровне агрегации

Все остальные модули импортируют эти данные через `from schedule_data import *`.
"""

from dataclasses import dataclass
import numpy as np

############################################################################
# Временная сетка
############################################################################

T = 288  # Число 5-минутных шагов в сутках: 24 часа × 60 мин / 5 мин = 288
period = range(T)  # Итератор по всем временным шагам [0, 1, ..., 287]

############################################################################
# Форматы фильмов
############################################################################

# Фильмы бывают двух форматов: A и B.
# Формат определяет, в каких залах можно показать фильм.
# Зал поддерживает формат, если формат входит в список hall.supported_formats.
movie_formats = set(["A", "B"])

@dataclass
class Movie:
	"""Фильм с заданными параметрами показа."""
	len: int            # Длительность фильма в 5-минутных шагах (12–36 → 1–3 часа)
	min: int            # Минимальное число сеансов фильма в расписании (гарантированный минимум)
	max: int            # Максимальное число сеансов фильма в расписании (ограничение сверху)
	format: str         # Формат фильма: "A" или "B"
	def __post_init__(self):
		# Проверяем, что формат допустим
		assert(self.format in movie_formats)

############################################################################
# Генерация фильмов
############################################################################

# Фиксированный seed для воспроизводимости результатов
np.random.seed(1)

# Создаём 20 фильмов: первые 10 формата A, остальные 10 — формата B.
# Длительность: случайная в диапазоне 12–36 шагов (1–3 часа).
# Минимум сеансов: 1 (фильм должен быть показан хотя бы один раз).
# Максимум сеансов: случайный от max(5, min) до 20.
movies = {}
for i in range(1, 21):
	fmt = "A" if i <= 10 else "B"                         # Формат: A для первых 10, B для остальных
	length = np.random.randint(12, 37)                     # Длительность в 5-минутных шагах
	min_shows = np.random.randint(1, 2)                    # Минимум 1 сеанс
	max_shows = np.random.randint(max(5, min_shows), 21)   # Максимум от 5 до 20 сеансов
	movies[f"movie_{i}"] = Movie(len=length, min=min_shows, max=max_shows, format=fmt)

movie_names = list(movies.keys())  # Список имён всех фильмов

############################################################################
# Залы
############################################################################

@dataclass
class Hall:
	"""Зал кинотеатра с поддержкой определённых форматов."""
	supported_formats: list[str]     # Список поддерживаемых форматов (подмножество {"A", "B"})
	max_shows: int                   # Максимальное число сеансов в зале за сутки
	def __post_init__(self):
		assert(type(self.supported_formats) is list)
		assert(all([f in movie_formats for f in self.supported_formats]))

# 10 залов:
#   hall_1 — hall_7: универсальные (поддерживают оба формата A и B)
#   hall_8 — hall_9: только формат A
#   hall_10:         только формат B
# Каждый зал вмещает не более 15 сеансов в сутки.
halls = {
	"hall_1": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_2": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_3": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_4": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_5": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_6": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_7": Hall(supported_formats=["A", "B"], max_shows=15),
	"hall_8": Hall(supported_formats=["A"], max_shows=15),
	"hall_9": Hall(supported_formats=["A"], max_shows=15),
	"hall_10": Hall(supported_formats=["B"], max_shows=15),
}
hall_names = list(halls.keys())  # Список имён всех залов

############################################################################
# Индексы совместимости залов и фильмов
############################################################################

# movie_halls[movie] = список залов, поддерживающих формат данного фильма.
# Пример: movie_halls["movie_1"] = ["hall_1", "hall_2", ..., "hall_9"] (формат A)
movie_halls = {
	movie: [hall for hall in halls if movies[movie].format in halls[hall].supported_formats]
	for movie in movies
}

# hall_movies[hall] = список фильмов, формат которых поддерживается данным залом.
# Пример: hall_movies["hall_8"] = ["movie_1", ..., "movie_10"] (только формат A)
hall_movies = {
	hall: [movie for movie in movies if movies[movie].format in halls[hall].supported_formats]
	for hall in halls
}

############################################################################
# Верхние границы числа сеансов (используются в оптимизации)
############################################################################

# Максимальное число сеансов каждого фильма во всех залах (из Movie.max).
movie_max_shows = {m: movies[m].max for m in movies}

# Максимальное число сеансов в каждом зале.
# Ограничено двумя факторами:
#   1. Заявленный максимум hall.max_shows (15)
#   2. Физическая вместимость: T / (min_len + 1) — сколько самых коротких
#      фильмов можно упаковать в зале с учётом 5-минутных перерывов.
# Берём минимум из этих двух ограничений.
hall_max_shows = {
	h: min(
		halls[h].max_shows,
		(T // (min(movies[m].len for m in hall_movies[h]) + 1)) + 1
	)
	for h in halls
}

# Максимальное число сеансов конкретного фильма в конкретном зале.
# Учитывает три ограничения:
#   1. Общий лимит сеансов фильма movie_max_shows[m]
#   2. Общий лимит сеансов в зале hall_max_shows[h]
#   3. Физическая вместимость: T / (len + 1) — сколько показов данного фильма
#      помещается в зале с учётом перерывов
# Если зал не поддерживает формат фильма, максимум = 0 (сеансы невозможны).
hall_movie_max_shows = {}
for h in halls:
	for m in movies:
		hall_movie_max_shows[h, m] = 0 if h not in movie_halls[m] else min(
			movie_max_shows[m],
			hall_max_shows[h],
			(T // (movies[m].len + 1)) + 1
		)

############################################################################
# Активные пары (зал, фильм) — только те, где сеансы возможны
############################################################################

# active_pairs — список пар (зал, фильм), для которых hall_movie_max_shows > 0.
# Используется как компактное представление пространства решений:
# вместо всех 10 × 20 = 200 пар, храним только допустимые.
active_pairs = [(h, m) for h in halls for m in movies if hall_movie_max_shows[h, m] > 0]
n_active = len(active_pairs)  # Число активных пар (переменных уровня 1)

############################################################################
# Допустимые времена начала
############################################################################

# valid_starts[movie] = range(0, T - len + 1) — все временные шаги, с которых
# можно начать показ фильма так, чтобы он закончился не позднее конца суток T.
# Например, при len=20 и T=288: valid_starts = range(269), т.е. можно начать
# в любой момент с 0:00 до 22:25.
valid_starts = {m: range(T - movies[m].len + 1) for m in movies}

############################################################################
# Ожидаемые продажи
############################################################################

# sales[movie][time] — ожидаемые продажи одного сеанса фильма movie,
# если он начинается в момент времени time.
# Значения от 1 до 9 (случайные), моделируют спрос в разное время суток.
# Это основная целевая функция: суммарные продажи = sum(sales[m][t]) по всем сеансам.
sales = {m: [np.random.randint(low=1, high=10) for t in range(T)] for m in movies}
